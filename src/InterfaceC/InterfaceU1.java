    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            final Registry registry = LocateRegistry.getRegistry("localhost", 1099);
            final String[] boundNames = registry.list();
            System.out.println(
                "Names bound to RMI registry at host " + "localhost" + " and port " + 1099 + ":");
            for (final String name : boundNames)
            {
                if(name.startsWith("RMI"))
                    choice1.add(name);                
            }
        }
        catch (RemoteException remoteEx)
        {
            System.err.println("RemoteException encountered: " + remoteEx.toString());
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

