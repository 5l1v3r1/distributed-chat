    public void setList1(ArrayList<String> clients, ArrayList<String> directClients){
        
        list1.removeAll();
        
        for(String client : clients)
            list1.add(client); 
        
        for(String combined : directClients){
            
            String arr[] = combined.split(" ", 2);
            String directClient = arr[0];
            
            if(!(directClient.equals( Global.cl.getNickname())))
                list1.add(directClient);
        }
    }
    
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        String message;
        
        message = jTextArea2.getText();
        if(list1.getSelectedItems().length == 0)
        {
            try {
                KruskalAlgorithm mst =  Global.cl.getRouterStub().getGraph().getMst();
                 Global.cl.getRouterStub().broadcastMsg( Global.cl.getNickname(), message, mst);
            } catch (RemoteException ex) {
                Logger.getLogger(InterfaceU1.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextArea2.setText(null);
        }
        else if (list1.getSelectedItems().length == 1){
        
            try {     
                Global.cl.getRouterStub().sendMessage(Global.cl.getNickname(), list1.getSelectedItem(), message);
            } catch (RemoteException ex) {
                Logger.getLogger(PrivateChat.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            for(String rcv : list1.getSelectedItems()) {
                
                try {     
                Global.cl.getRouterStub().sendMessage(Global.cl.getNickname(), rcv, message);
            } catch (RemoteException ex) {
                Logger.getLogger(PrivateChat.class.getName()).log(Level.SEVERE, null, ex);
            }
                
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

